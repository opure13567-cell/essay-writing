export function buildPrompt(data) {
  const { personal_info, answers } = data

  const optionLabels = {
    rural: '农村/乡镇',
    small_city: '小城市/县城',
    big_city: '大中城市',

    poverty: '脱贫攻坚——从贫困到小康的历史性转变',
    rural_revitalization: '乡村振兴——基础设施改善、产业发展、村容村貌提升',
    ecology: '生态环境——绿色发展理念下的环境改善',
    technology: '科技创新——数字经济、智慧城市建设、新基建',
    culture: '文化传承——传统文化保护与精神文明建设',
    transport: '交通发展——高铁、高速、农村公路建设',

    yes_specific: '能举出具体的家乡变化例子，细节丰富',
    yes_vague: '有大致印象但细节需要补充',
    need_help: '需要AI帮助构思合理的变化案例',

    plain: '文风朴实真诚，用平实语言讲述真实变化',
    elevated: '文风立意高远，充分论述中国特色社会主义制度优势',
    balanced: '文风以具体变化为基础，自然联系到制度层面',
  }

  const title = personal_info.title || `从${personal_info.hometown_name || '家乡'}的变迁看中国特色社会主义制度优势`

  return `你是一名大学本科生，正在完成"形势与政策"课程的暑假作业。请根据以下信息，撰写一篇《从家乡变迁看中国特色社会主义制度优势》的社会调查报告。

## 个人信息（用于报告封面）
- 姓名：${personal_info.name}
- 学校/学院：${personal_info.school}
- 专业及班级：${personal_info.major}
- 年级：${personal_info.grade}
- 学号：${personal_info.student_id}

## 家乡素材（基于访谈问卷）
- 家乡所在地：${personal_info.hometown_name || '（由AI适当发挥，不要写具体村镇名）'}
- 家乡类型：${optionLabels[answers.area_type] || answers.area_type}
- 关注角度：${optionLabels[answers.focus_angle] || answers.focus_angle}
- 案例丰富度：${optionLabels[answers.has_example] || answers.has_example}
- 写作风格：${optionLabels[answers.tone] || answers.tone}

## 报告要求

### 格式规范
- 报告标题：${title}（宋体三号加粗，居中）
- 个人信息行：姓名、学院、专业、年级、班级、学号（宋体小四号，居中）
- 摘要+关键词：楷体小四号，约200字摘要，3-5个关键词
- 正文：宋体四号，固定行距25磅，页边距上下左右2.5cm
- 总字数：约5000字

### 报告结构
一、引言（约500字）——调查背景、目的、方法，家乡概况简述
二、家乡发展变迁的具体表现（约1500字）——围绕选定角度展开，列举具体变化和数据
三、家乡变迁背后的制度支撑（约1200字）——从政策、党的领导、制度优势角度分析
四、家乡变迁中的人民获得感（约800字）——群众的感受、生活的改善
五、制度优势与新时代展望（约800字）——中国特色社会主义制度优越性、未来展望
六、结语（约300字）——总结全文
附录：访谈提纲

### 写作要求
1. 以第一人称"我"来写，模拟真实大学生的口吻——不是优秀学生，是普通学生的水平
2. 禁止使用任何Markdown格式标记
3. 句式长短交错，偶尔插入口语化的短句，像真人写的时候想到什么说什么
4. 每段开头要变换方式，不要千篇一律
5. 适当加入"说实话""我觉得""真的""挺…的"等口语表达
6. 偶尔加入一个不完美但合理的表达（如轻微的重复、口语化转折）
7. 不写分点列举，用自然段落
8. 恰当引用国家政策，但要自然融入，不要像在背政治书
9. 文中涉及家乡地点时，只写地级市/县级层面，不出现具体村镇名或小区名
10. 关键：避免使用AI高频词汇——"不仅…更…""随着…的发展""值得注意的是""毋庸置疑""综上所述""首先其次最后"

### 防AI识别要求（非常重要）
- 不要在文中提到作者的年龄、出生年份、具体年级
- 不要写"作为一名大一/大二学生"等明确身份表述
- 不要编造具体的家庭成员信息和家庭故事
- 不要用"回想起X年前"等暴露年龄的表述
- 保持自然的调查报告风格，像真人学生采访调研后写的
- 不要让文章结构过于清晰——真人大学生的报告会有一些散乱`
}
