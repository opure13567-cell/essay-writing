export function buildPrompt(data) {
  const { personal_info, answers } = data

  const optionLabels = {
    familiar: '对目标行业比较熟悉，有实际人脉资源',
    somewhat: '对目标行业有一定了解，通过多种渠道接触过',
    curious: '对目标行业充满好奇，希望通过深度访谈来了解',

    career_path: '职业发展路径',
    industry_prospect: '行业前景与发展趋势',
    work_content: '工作内容与技能要求',
    salary_welfare: '薪酬福利与晋升',
    work_life: '工作感悟与体验',
    entry_advice: '入行建议与忠告',

    professional: '专业客观的调查报告风格',
    storytelling: '以访谈故事为主线的生动叙述风格',
    balanced: '专业分析与故事叙述相结合',
  }

  const industry = personal_info.industry || answers.industry || '目标行业'
  const title = personal_info.title || `${industry}领域从业人物访谈报告`

  return `你是一名大学本科生（2023级），正在完成"形势与政策"课程的暑假作业。请根据以下信息，撰写一篇《行业人物访谈报告》。

## 个人信息（用于报告封面）
- 姓名：${personal_info.name}
- 学校/学院：${personal_info.school}
- 专业及班级：${personal_info.major}
- 年级：${personal_info.grade || '2023级'}
- 学号：${personal_info.student_id}

## 访谈素材（基于问卷）
- 目标行业/领域：${personal_info.industry || '（未指定）'}
- 职业目标：${personal_info.career_goal || '（未指定）'}
- 行业了解程度：${optionLabels[answers.industry_level] || answers.industry_level}
- 最关注的访谈方面：${optionLabels[answers.focus_areas] || answers.focus_areas}
- 写作风格：${optionLabels[answers.tone] || answers.tone}

## 报告要求

### 格式规范
- 报告标题：${title}（宋体三号加粗，居中）
- 个人信息行：姓名、学院、专业、年级、班级、学号（宋体小四号，居中）
- 摘要+关键词：楷体小四号，约200字摘要，3-5个关键词
- 正文：宋体四号，固定行距25磅，页边距上下左右2.5cm
- 总字数：5000-10000字

### 报告结构
一、引言（约500-800字）——调查背景、目的、方法（本次采用访谈法，选择三位${industry}领域从业者进行深度访谈）、访谈对象简介
二、行业概述（约800-1000字）——${industry}行业的发展现状、主要特点、就业形势
三、访谈实录与分析（约2500-4000字）——分别呈现三位访谈对象的访谈内容，从行业特点、工作性质、工作经历、工作内容、行业发展前景、福利薪酬、工作感悟等角度展开
四、访谈发现与思考（约800-1000字）——三位访谈对象的共性与差异分析，对行业的整体认知
五、个人职业规划启示（约500-800字）——访谈对个人未来择业/创业/升学的启示和影响
六、结语（约300-500字）——总结访谈收获，展望未来发展
附录：访谈提纲

### 写作要求（让文章更像真人学生写的）
1. 以第一人称"我"来写，模拟真实大学生的口吻——不是优秀学生，是普通学生的水平
2. 禁止使用任何Markdown格式标记
3. 句式长短交错，偶尔插入口语化的短句
4. 每段开头要变换方式，不要千篇一律
5. 适当加入"说实话""我觉得""真的""挺…的""听完之后"等口语表达
6. 偶尔加入一个不完美但合理的表达（如轻微的重复、口语化转折）
7. 关键：避免使用AI高频词汇——"不仅…更…""随着…的发展""值得注意的是""毋庸置疑""综上所述""首先其次最后"
8. 三位访谈对象要有不同的性格和说话风格，让采访更真实
9. 访谈问答部分可以用一问一答的形式呈现，显得更真实

### 防AI识别与防辅导员识破规则（严格遵守）
- 不要在文中提到作者的年龄、出生年份
- 不要写"作为一名大X学生"等明确身份表述
- 不要编造具体的公司全称和地址（用"某互联网大厂""一家金融公司"等模糊表述）
- 不要用"回想起X年前"等暴露年龄的表述
- 访谈对象的姓名用"张工""李经理""王老师"等模糊称呼
- 保持自然的调查报告风格，不要过于完美`
}
